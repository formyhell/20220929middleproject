package admin.vo;

public class instructorVO {
	private String instructorId;
	private String memMail;
	private String teachTel;
	private String teachSubject;
	private String teachIntrod;
	private String teachLink;
	
	public String getInstructorId() {
		return instructorId;
	}
	public void setInstructorId(String instructorId) {
		this.instructorId = instructorId;
	}
	public String getMemMail() {
		return memMail;
	}
	public void setMemMail(String memMail) {
		this.memMail = memMail;
	}
	public String getTeachTel() {
		return teachTel;
	}
	public void setTeachTel(String teachTel) {
		this.teachTel = teachTel;
	}
	public String getTeachSubject() {
		return teachSubject;
	}
	public void setTeachSubject(String teachSubject) {
		this.teachSubject = teachSubject;
	}
	public String getTeachIntrod() {
		return teachIntrod;
	}
	public void setTeachIntrod(String teachIntrod) {
		this.teachIntrod = teachIntrod;
	}
	public String getTeachLink() {
		return teachLink;
	}
	public void setTeachLink(String teachLink) {
		this.teachLink = teachLink;
	}
	
	
}
